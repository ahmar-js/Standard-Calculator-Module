class BasicCalc:
    def __init__(self, num1, num2):
        self.first = num1
        self.second = num2

    def addition(self):
        return self.first + self.second

    def subtraction(self):
        return self.first - self.second

    def multiplication(self):
        return self.first * self.second

    def division(self):
        return self.first / self.second